          <h3 id='不正ログイン監査'>
            不正ログイン監査
          </h3>

          <p>当システムは、ログインに異常がないかを自動的に監査します。</p>

          <p>例えば、ユーザーが普段使用しているブラウザやデバイスを変更したり、新しい場所からログインしようとしたりした場合、システムが認識してユーザーに通知します。また、LPSecretBaseプラットフォームは、一度に1つのログインのみを許可することで、同時ログインを制限します。さらに、アカウントにログインしてから4時間後には自動的にログアウトされ、再度ログインを求められます。当システムは、ユーザーの身元を確認するために、アカウントへの再ログインが必要になります。</p>

